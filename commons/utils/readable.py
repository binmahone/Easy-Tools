def to_memory_readable(b: int) -> str:
    if b < 1024:  # 比特
        b = str(round(b, 2)) + ' B'  # 字节
    elif 1024 <= b < 1024 * 1024:
        b = str(round(b / 1024, 2)) + ' KB'  # 千字节
    elif 1024 * 1024 <= b < 1024 * 1024 * 1024:
        b = str(round(b / 1024 / 1024, 2)) + ' MB'  # 兆字节
    elif 1024 * 1024 * 1024 <= b < 1024 * 1024 * 1024 * 1024:
        b = str(round(b / 1024 / 1024 / 1024, 2)) + ' GB'  # 千兆字节
    elif 1024 * 1024 * 1024 * 1024 <= b < 1024 * 1024 * 1024 * 1024 * 1024:
        b = str(round(b / 1024 / 1024 / 1024 / 1024, 2)) + ' TB'  # 太字节
    elif 1024 * 1024 * 1024 * 1024 * 1024 <= b < 1024 * 1024 * 1024 * 1024 * 1024 * 1024:
        b = str(round(b / 1024 / 1024 / 1024 / 1024 / 1024, 2)) + ' PB'  # 拍字节
    elif 1024 * 1024 * 1024 * 1024 * 1024 * 1024 <= b < 1024 * 1024 * 1024 * 1024 * 1024 * 1024 * 1024:
        b = str(round(b / 1024 / 1024 / 1024 / 1024 / 1024 / 1024, 2)) + ' EB'  # 艾字节
    return b
